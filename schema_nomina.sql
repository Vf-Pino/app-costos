-- 4. Tabla: payroll_liquidations (Liquidaciones Históricas — Inmutables)
CREATE TABLE IF NOT EXISTS casa_bistro_analitica.payroll_liquidations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID NOT NULL REFERENCES casa_bistro_analitica.employees(id) ON DELETE CASCADE,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    total_minutes_worked INTEGER NOT NULL DEFAULT 0,
