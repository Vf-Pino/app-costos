-- 1. Tabla: employees (Empleados)
CREATE TABLE IF NOT EXISTS casa_bistro_analitica.employees (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    hourly_rate NUMERIC(10, 2) NOT NULL DEFAULT 0,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE casa_bistro_analitica.employees ENABLE ROW LEVEL SECURITY;

-- Política: usuarios autenticados pueden leer empleados
DROP POLICY IF EXISTS "Usuarios autenticados pueden leer empleados" ON casa_bistro_analitica.employees;
CREATE POLICY "Usuarios autenticados pueden leer empleados"
  ON casa_bistro_analitica.employees FOR SELECT TO authenticated USING (true);

-- Política: solo admin puede crear/editar/desactivar empleados
-- (El control de admin se maneja en la app; RLS permite todo a autenticados por ahora)
DROP POLICY IF EXISTS "Usuarios autenticados pueden modificar empleados" ON casa_bistro_analitica.employees;
CREATE POLICY "Usuarios autenticados pueden modificar empleados"
  ON casa_bistro_analitica.employees FOR ALL TO authenticated USING (true);

GRANT ALL PRIVILEGES ON TABLE casa_bistro_analitica.employees TO anon, authenticated, service_role;

-- 2. Tabla: time_logs (Registro de Asistencia)
-- Regla de negocio: máximo 1 turno abierto por empleado por día
CREATE TABLE IF NOT EXISTS casa_bistro_analitica.time_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID NOT NULL REFERENCES casa_bistro_analitica.employees(id) ON DELETE CASCADE,
    date DATE NOT NULL DEFAULT CURRENT_DATE,
    check_in TIMESTAMPTZ NOT NULL DEFAULT now(),
    check_out TIMESTAMPTZ,
    total_minutes INTEGER,
    -- Restricción: solo un turno abierto (sin check_out) por empleado por día
    CONSTRAINT uq_one_open_shift_per_day UNIQUE NULLS NOT DISTINCT (employee_id, date, check_out)
);

ALTER TABLE casa_bistro_analitica.time_logs ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Usuarios autenticados pueden leer time_logs" ON casa_bistro_analitica.time_logs;
CREATE POLICY "Usuarios autenticados pueden leer time_logs"
  ON casa_bistro_analitica.time_logs FOR SELECT TO authenticated USING (true);

DROP POLICY IF EXISTS "Usuarios autenticados pueden modificar time_logs" ON casa_bistro_analitica.time_logs;
CREATE POLICY "Usuarios autenticados pueden modificar time_logs"
  ON casa_bistro_analitica.time_logs FOR ALL TO authenticated USING (true);

GRANT ALL PRIVILEGES ON TABLE casa_bistro_analitica.time_logs TO anon, authenticated, service_role;

-- 3. Tabla: debts_and_advances (Deudas y Adelantos)
CREATE TABLE IF NOT EXISTS casa_bistro_analitica.debts_and_advances (
