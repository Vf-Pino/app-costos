DROP POLICY IF EXISTS "Solo insertar liquidaciones" ON casa_bistro_analitica.payroll_liquidations;
CREATE POLICY "Solo insertar liquidaciones"
  ON casa_bistro_analitica.payroll_liquidations FOR INSERT TO authenticated WITH CHECK (true);

GRANT ALL PRIVILEGES ON TABLE casa_bistro_analitica.payroll_liquidations TO anon, authenticated, service_role;

-- ==========================================
-- Función y Trigger: Cálculo automático de minutos
-- ==========================================

CREATE OR REPLACE FUNCTION casa_bistro_analitica.calculate_total_minutes()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.check_out IS NOT NULL THEN
        NEW.total_minutes := EXTRACT(EPOCH FROM (NEW.check_out - NEW.check_in)) / 60;
    ELSE
        NEW.total_minutes := NULL;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_calculate_total_minutes ON casa_bistro_analitica.time_logs;
CREATE TRIGGER trg_calculate_total_minutes
BEFORE INSERT OR UPDATE OF check_in, check_out
ON casa_bistro_analitica.time_logs
FOR EACH ROW
EXECUTE FUNCTION casa_bistro_analitica.calculate_total_minutes();
